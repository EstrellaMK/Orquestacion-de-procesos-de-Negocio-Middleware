    public void mostrartabla(String dni) {
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id Factura");
        modelo.addColumn("Id Cliente");
        modelo.addColumn("Cliente");
        modelo.addColumn("RUC");
        modelo.addColumn("Id Articulo");
        modelo.addColumn("Articulo");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");
        modelo.addColumn("Total sin IGV");
        modelo.addColumn("Total a cobrar");
        modelo.addColumn("Fecha");
        modelo.addColumn("Estado");
        

        lista.setModel(modelo);
        String sql = "";
        sql = "SELECT * FROM fisiutiles.tt_data where idCliente = "+dni;
        String datos[] = new String[12];//Ya que hay 5 datos en la tabla Tutor 
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {

                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);
                datos[9] = rs.getString(10);
                datos[10] = rs.getString(11);
                datos[11] = rs.getString(12);
                
                modelo.addRow(datos);//Se guarda el nuevo modelo
            }

            lista.setModel(modelo);
        } catch (SQLException ex) {

        }
    }
    
    
    
